function typographAccordionLine(line) {
  return typeof line === "string" ? normalizeLegalReferences(typograph(line)) : line;
}

function typographAccordionItem(item) {
  return {
    label: normalizeLegalReferences(typograph(item.label)),
    lines: item.lines.map(typographAccordionLine),
  };
}

